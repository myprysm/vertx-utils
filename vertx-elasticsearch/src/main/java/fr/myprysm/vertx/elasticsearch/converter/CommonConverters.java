    /**
     * Convert a {@link JsonObject} into an {@link XContentParser}.
     *
     * @param json the json to convert
     * @return the parser
     */
    public static XContentParser toXContent(JsonObject json) {
        try {
            return XContentType.JSON.xContent().createParser(
                    registry,
                    LoggingDeprecationHandler.INSTANCE,
                    json.toBuffer().getBytes()
            );
        } catch (IOException exc) {
            throw new ConverterException("Unable to convert JsonObject to XContentParser.", exc);
        }
    }

    /**
     * Convert a {@link ToXContent} object into a {@link JsonObject}.
     *
     * @param xContent the content to convert
     * @return the JsonObject
     */
    public static JsonObject fromXContent(ToXContent xContent) {
        try {
            BytesRef bytesRef = XContentHelper.toXContent(xContent, XContentType.JSON, false).toBytesRef();
            return Buffer
                    .buffer(bytesRef.bytes)
                    .toJsonObject();
        } catch (Exception exc) {
            throw new ConverterException("Unable to convert ToXContent to JsonObject.", exc);
        }
    }

    /**
     * Convert an Object into a Boolean whenever possible.
     * <p>
     * Returns <code>null</code> otherwise.
     *
     * @param object the object
     * @return the boolean
     */
    public static Boolean objectToBoolean(Object object) {
        if (object instanceof Boolean) {
            return (Boolean) object;
        } else if (object instanceof String) {
            return BooleanUtils.toBoolean((String) object);
        } else if (object instanceof Number) {
            return BooleanUtils.toBoolean(((Number) object).intValue());
        } else {
            return null;
        }
    }


    /**
     * Extracts the headers from a request.
     *
     * @param request the request
     * @return the headers
     */
    public static Header[] headersFromRequest(BaseRequest request) {
        requireNonNull(request);

        Map<String, String> headers = request.getHeaders();
        if (headers == null) {
            return new Header[]{};
        }

        return headersFromMap(headers);
    }

    /**
     * Convert a Map into a headers array.
     *
     * @param map the map
     * @return the headers array.
     */
    public static Header[] headersFromMap(Map<String, String> map) {
        requireNonNull(map);
        return map.entrySet().stream().map(entry -> new BasicHeader(entry.getKey(), entry.getValue())).toArray(Header[]::new);
    }


    /**
     * Convert an HttpHost DataObject into an HttpHost.
     *
     * @param host the data object host
     * @return the host
     */
    public static org.apache.http.HttpHost httpHostToES(HttpHost host) {
        org.apache.http.HttpHost httpHost;
        if (host.getPort() == null && host.getProtocol() == null) {
            httpHost = org.apache.http.HttpHost.create(host.getHostname());
        } else {
            if (StringUtils.isNotBlank(host.getProtocol())) {
                httpHost = new org.apache.http.HttpHost(host.getHostname(), host.getPort(), host.getProtocol());
            } else {
                httpHost = new org.apache.http.HttpHost(host.getHostname(), host.getPort());
            }
        }

        return httpHost;
    }


    public static String charSequenceToString(CharSequence in) {
        if (in == null) {
            return null;
        } else if (in instanceof String) {
            return (String) in;
        } else {
            return in.toString();
        }
    }

